#           0      1       2       3
colores=("rojo","verde","azul","amarillo")

print(colores)  # ['rojo', 'verde', 'azul', 'amarillo']

print(colores[0])  # rojo
print(colores[3])  # amarillo
print(colores[0:2])  # desde la posicion 0, muestre dos colores  ['rojo', 'verde']
print(colores[-1]) # amarillo (el ultimo color)
print(colores[:2]) # ['rojo', 'verde']
print(colores[:-2]) # ['rojo', 'verde']
print(colores[2:]) # ['azul', 'amarillo']
print(len(colores)) # len me devuelve el largo de valores.
print(colores[len(colores)-1])   # el ultimo color.

